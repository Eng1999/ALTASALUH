if (finalMenu && finalNav) {
  finalMenu.addEventListener("click", () => {
    const open = finalNav.classList.toggle("open");
    finalMenu.setAttribute("aria-expanded", String(open));
  });
}

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: .14 });
document.querySelectorAll(".reveal").forEach((node) => observer.observe(node));

document.querySelectorAll(".hero-stats strong, .hero-stats span, .card > span, .media-card span").forEach((node) => {
  if (node.dataset.count) return;
  const raw = node.textContent.trim();
  const match = raw.match(/^(\D*)(\d+)(\D*)$/);
  if (!match) return;
  node.dataset.prefix = match[1] || "";
  node.dataset.count = match[2];
  node.dataset.suffix = match[3] || "";
  node.dataset.pad = String(match[2].length);
});

document.querySelectorAll("[data-count]").forEach((node) => {
  const target = Number(node.dataset.count || 0);
  const formatValue = (value) => {
    const pad = Number(node.dataset.pad || 0);
    const raw = pad > 1 ? String(value).padStart(pad, "0") : value.toLocaleString("en-US");
    return `${node.dataset.prefix || ""}${raw}${node.dataset.suffix || ""}`;
  };
  node.textContent = formatValue(0);
  const countObserver = new IntersectionObserver((entries) => {
    if (!entries[0].isIntersecting) return;
    const start = performance.now();
    const tick = (now) => {
      const progress = Math.min((now - start) / 2200, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      node.textContent = formatValue(Math.round(target * eased));
      if (progress < 1) requestAnimationFrame(tick);
    };
    requestAnimationFrame(tick);
    countObserver.disconnect();
  });
  countObserver.observe(node);
});

document.querySelectorAll(".contact-form").forEach((form) => {
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const status = form.querySelector(".form-status");
    if (status) status.textContent = translations[html.lang].formSuccess;
    form.reset();
  });
});
