const html = document.documentElement;
html.classList.add("js");
const toggle = document.querySelector("[data-lang-toggle]");
const savedLanguage = localStorage.getItem("altasaluh-language") || "ar";
const documentFiles = {
  protection: {
    titleKey: "docProtectionTitle",
    file: "assets/docs/financial-customers-protection.pdf"
  },
  collection: {
    titleKey: "docCollectionTitle",
    file: "assets/docs/collection-rules-individuals.pdf"
  },
  profile: {
    titleKey: "docProfileTitle",
    file: "assets/docs/company-profile-2026.pdf"
  }
};
let currentDocKey = documentFiles[location.hash.slice(1)] ? location.hash.slice(1) : "protection";

function updateDocViewer(key, pushHash = false) {
  if (!documentFiles[key]) return;
  currentDocKey = key;
  const doc = documentFiles[key];
  const title = translations[html.lang]?.[doc.titleKey] || "";
  const titleNode = document.querySelector("[data-i18n='docsTitle']");
  const frame = document.querySelector("[data-doc-frame]");
  const open = document.querySelector("[data-doc-open]");
  const download = document.querySelector("[data-doc-download]");

  if (titleNode) titleNode.textContent = title;
  if (frame) {
    frame.title = title;
    frame.src = `${doc.file}#toolbar=1&navpanes=1&view=FitH`;
  }
  if (open) open.href = doc.file;
  if (download) download.href = doc.file;
  document.querySelectorAll("[data-doc-card]").forEach((card) => {
    card.classList.toggle("active", card.dataset.docCard === key);
  });
  if (pushHash) history.replaceState(null, "", `#${key}`);
}

function applyLanguage(lang) {
  html.lang = lang;
  html.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.dir = html.dir;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = translations[lang][node.dataset.i18n];
    if (value) node.textContent = value;
  });
  if (toggle) toggle.textContent = lang === "ar" ? "English" : "العربية";
  localStorage.setItem("altasaluh-language", lang);
  updateDocViewer(currentDocKey);
}
applyLanguage(savedLanguage);
if (toggle) toggle.addEventListener("click", () => applyLanguage(html.lang === "ar" ? "en" : "ar"));

document.querySelectorAll("[data-doc-link]").forEach((link) => {
  link.addEventListener("click", (event) => {
    const key = link.dataset.docLink;
    if (!documentFiles[key]) return;
    event.preventDefault();
    updateDocViewer(key, true);
    document.querySelector(".docs-page")?.scrollIntoView({ behavior: "smooth", block: "start" });
  });
});
window.addEventListener("hashchange", () => {
  const key = location.hash.slice(1);
  if (documentFiles[key]) updateDocViewer(key);
});

const nav = document.querySelector(".nav-links");
